package javaNewAgg;

public class Address {
	
	int flatno;
	String buildingName;
	String streetName;
	int zipCode;
	
	
	public Address(int flatno, String buildingName, String streetName, int zipCode) {
		super();
		this.flatno = flatno;
		this.buildingName = buildingName;
		this.streetName = streetName;
		this.zipCode = zipCode;
	}
}

